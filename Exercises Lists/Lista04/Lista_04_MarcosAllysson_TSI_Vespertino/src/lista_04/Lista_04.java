package lista_04;

import javax.swing.JOptionPane;

public class Lista_04 {

    public static void main(String[] args) {
        JOptionPane.showMessageDialog(null,"Resposta Questão 1: \n\n While: Este comando é executado enquanto tal condição for verdadeira ou falsa. \n"
                + "Do While: Este comando é executado pelo menos uma vez, e depois verifica condição. Com isso, continua fazendo alguma coisa enquanto condição for verdadeira ou falsa \n"
                + "For: Este comando é executado quando se sabe quantas vezes um comando terá que ser feito. Ou seja, quando se sabe o início e o final, executa alguma coisa.");
    }
    
}
